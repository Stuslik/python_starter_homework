print('Программа решения квадратного уравнения: ax**2 + bx + c = 0')
a = int(input('Введите первую переменную а:\n'))
b = int(input('Введите первую переменную b:\n'))
c = int(input('Введите первую переменную c:\n'))
print('\nОтвет:')
x1 = (-b + (b ** 2 - 4 * a * c) ** 0.5)/(2 * a)
x2 = (-b - (b ** 2 - 4 * a * c) ** 0.5)/(2 * a)
print('x1 = ', x1)
print('x2 = ', x2)